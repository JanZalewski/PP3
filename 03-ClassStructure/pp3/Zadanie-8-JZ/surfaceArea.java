public class surfaceArea
{
   static double circleArea(double radius) {
       return 3.14*radius*radius;
   };
   static double rectangleArea(double s1, double s2) {
       return s1*s2;
   };
   static double triangleArea(double s, double h) {
       return s*h/2;
   };
       
}
